    private void jButton_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GuardarActionPerformed
  // Obtener los datos del formulario
    String nombre = txt_nombre.getText();
    String apellido = txt_apellido.getText();
    String cedula = txt_cedula.getText();
    String telefono = txt_telefono.getText();
    
    // Verificar si los campos no están vacíos
    if (nombre.isEmpty() || apellido.isEmpty() || cedula.isEmpty() || telefono.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        // Llamar al método para insertar en la base de datos
        if (guardarCliente(nombre, apellido, cedula, telefono)) {
            JOptionPane.showMessageDialog(this, "Cliente agregado exitosamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            Limpiar();  // Limpiar los campos después de guardar
        } else {
            JOptionPane.showMessageDialog(this, "Error al agregar cliente", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
  
    }//GEN-LAST:event_jButton_GuardarActionPerformed

