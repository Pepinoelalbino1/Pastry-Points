private void Limpiar() {
    txt_nombre.setText("");
    txt_apellido.setText("");
    txt_cedula.setText("");
    txt_telefono.setText("");
}
private boolean guardarCliente(String nombre, String apellido, String cedula, String telefono) {
    Connection conn = null;
    PreparedStatement stmt = null;
    boolean exito = false;

    try {
        // Usar la clase Conexion para obtener la conexión a la base de datos
        conn = Conexion.getInstance().getConnection(); 

        String sql = "INSERT INTO Clientes (nombre, apellido, cedula, telefono) VALUES (?, ?, ?, ?)";
        stmt = conn.prepareStatement(sql);
        stmt.setString(1, nombre);
        stmt.setString(2, apellido);
        stmt.setString(3, cedula);
        stmt.setString(4, telefono);

        int filasAfectadas = stmt.executeUpdate();
        if (filasAfectadas > 0) {
            exito = true; // Si se insertaron datos exitosamente
        }
    } catch (SQLException e) {
        e.printStackTrace();
    } finally {
        // Cerrar los recursos
        try {
            if (stmt != null) stmt.close();
            if (conn != null) conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    return exito;
}
